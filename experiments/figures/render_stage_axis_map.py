#!/usr/bin/env python3
from __future__ import annotations

"""Render a data-driven early-entry vs continuation-stability overview map.

The paper's teaser should teach the stage decomposition in a few seconds. This
script rebuilds the TikZ source from the same retained JSON artifacts used by
Figure 2 / Table 1 rather than hard-coding decorative points.
"""

import json
from dataclasses import dataclass
from pathlib import Path
from typing import Iterable

ROOT = Path(__file__).resolve().parents[2]
FIG_DIR = ROOT / "paper" / "figures"
OUT_TEX = FIG_DIR / "fig_stage_axis_map_tikz.tex"
OUT_JSON = ROOT / "outputs" / "stage_axis_map_points_2026-04-28.json"


@dataclass(frozen=True)
class LangSpec:
    pair: str
    label: str
    short: str
    dx: float
    dy: float


LANGS = [
    LangSpec("aksharantar_hin_latin", "Hindi", "Hi", -0.08, 0.045),
    LangSpec("aksharantar_mar_latin", "Marathi$^{\\dagger}$", "Mr$^\\dagger$", -0.13, 0.045),
    LangSpec("aksharantar_ben_latin", "Bengali", "Bn", 0.055, 0.040),
    LangSpec("aksharantar_tam_latin", "Tamil", "Ta", -0.070, 0.045),
    LangSpec("aksharantar_tel_latin", "Telugu", "Te", -0.060, -0.065),
]

MODEL_STYLE = {
    "1b": {"label": "Gemma 3 1B", "color": "appleOrange", "mark": "*", "size": "2.75pt"},
    "4b": {"label": "Gemma 3 4B", "color": "appleBlue", "mark": "square*", "size": "2.75pt"},
}


def _read(path: Path) -> dict:
    return json.loads(path.read_text(encoding="utf-8"))


def _artifact_paths(model: str, pair: str) -> list[Path]:
    if pair == "aksharantar_mar_latin":
        path = ROOT / "research/results/loop2_vm_controls/expansion_core64_seed42/raw" / model / pair / "nicl64/neutral_filler_recency_controls.json"
        return [path] if path.exists() else []
    return sorted(
        (ROOT / "research/results/four_lang_thesis_panel").glob(
            f"seed*/raw/{model}/{pair}/nicl64/neutral_filler_recency_controls.json"
        )
    )


def _mean(values: Iterable[float]) -> float:
    xs = [float(x) for x in values]
    if not xs:
        raise ValueError("Cannot average an empty sequence")
    return sum(xs) / len(xs)


def _point(model: str, lang: LangSpec) -> dict:
    rows = []
    for path in _artifact_paths(model, lang.pair):
        payload = _read(path)
        helpful = payload["summary_by_condition"]["icl_helpful"]
        raw_tail_cer = helpful.get("mean_continuation_fidelity")
        rows.append(
            {
                "path": str(path.relative_to(ROOT)),
                "first_entry": float(helpful["mean_first_entry_correct"]),
                # Historical artifact key is named "continuation_fidelity" but stores continuation-tail CER.
                # It is undefined for cells with no correct first-akshara entries; those cells are plotted at
                # zero continuation stability below rather than silently dropped from the early axis.
                "tail_cer": None if raw_tail_cer is None else float(raw_tail_cer),
                "exact_match": float(helpful["mean_exact_match"]),
                "full_cer": float(helpful["mean_akshara_cer"]),
            }
        )
    if not rows:
        raise FileNotFoundError(f"No retained artifacts found for {model} {lang.pair}")
    first_entry = _mean(row["first_entry"] for row in rows)
    finite_tail_cers = [row["tail_cer"] for row in rows if row["tail_cer"] is not None]
    tail_cer = _mean(finite_tail_cers) if finite_tail_cers else 1.0
    stability = max(0.0, min(1.0, 1.0 - tail_cer))
    return {
        "model": model,
        "model_label": MODEL_STYLE[model]["label"],
        "pair": lang.pair,
        "language": lang.label,
        "short": lang.short,
        "n_seeds": len(rows),
        "first_entry": first_entry,
        "tail_cer": tail_cer,
        "continuation_stability": stability,
        "exact_match": _mean(row["exact_match"] for row in rows),
        "full_cer": _mean(row["full_cer"] for row in rows),
        "support": [row["path"] for row in rows],
        "dx": lang.dx,
        "dy": lang.dy,
    }


def _tikz(points: list[dict]) -> str:
    lines: list[str] = []
    lines.append("% Auto-generated by experiments/render_stage_axis_map.py")
    lines.append("\\begin{tikzpicture}")
    lines.append(
        r"\begin{axis}["
        r"width=7.65cm,height=4.55cm,"
        r"xmin=-0.02,xmax=1.04,ymin=-0.03,ymax=1.04,"
        r"xlabel={target-entry success $E_{\mathrm{ak}}$},"
        r"ylabel={continuation stability},"
        r"xtick={0,0.25,0.5,0.75,1.0},ytick={0,0.25,0.5,0.75,1.0},"
        r"grid=major,major grid style={line width=.16pt,draw=appleLightGray},"
        r"axis line style={draw=appleGray},tick style={draw=appleGray},"
        r"label style={font=\scriptsize},tick label style={font=\scriptsize},"
        r"clip=false,"
        r"legend style={draw=appleLightGray,fill=white,fill opacity=0.94,text opacity=1,font=\tiny,"
        r"at={(0.03,0.97)},anchor=north west,rounded corners=1pt,inner sep=1.2pt},"
        r"legend cell align={left}]"
    )

    # Light quadrant language teaches the stage interpretation without covering data.
    lines.append(r"\fill[appleOrange!4] (axis cs:-0.02,-0.03) rectangle (axis cs:0.60,0.45);")
    lines.append(r"\fill[appleBlue!3] (axis cs:0.60,0.45) rectangle (axis cs:1.04,1.04);")
    lines.append(r"\fill[appleGreen!3] (axis cs:0.60,-0.03) rectangle (axis cs:1.04,0.45);")
    lines.append(r"\draw[densely dashed,appleGray!60,line width=.42pt] (axis cs:0.60,-0.03) -- (axis cs:0.60,1.04);")
    lines.append(r"\draw[densely dashed,appleGray!60,line width=.42pt] (axis cs:-0.02,0.45) -- (axis cs:1.04,0.45);")

    # Draw non-highlighted cells first, with just enough opacity to show the map.
    for model in ["1b", "4b"]:
        style = MODEL_STYLE[model]
        coords = " ".join(
            f"({p['first_entry']:.3f},{p['continuation_stability']:.3f})"
            for p in points
            if p["model"] == model
        )
        lines.append(
            rf"\addplot+[only marks,mark={style['mark']},mark size={style['size']},"
            rf"draw={style['color']}!90,fill={style['color']}!78,opacity=0.88] coordinates {{{coords}}};"
        )
        lines.append(rf"\addlegendentry{{{style['label']}}}")

    # Keep the overview readable: label only the two case-study cells.
    case_specs = [
        ("1b", "aksharantar_hin_latin", "Hindi 1B", -0.06, -0.12),
        ("1b", "aksharantar_tel_latin", "Telugu 1B", 0.15, -0.12),
    ]
    for model, pair, label, dx, dy in case_specs:
        point = next(p for p in points if p["model"] == model and p["pair"] == pair)
        color = MODEL_STYLE[model]["color"]
        lines.append(
            rf"\addplot+[only marks,mark=o,mark size=4.35pt,draw={color},line width=.68pt] "
            rf"coordinates {{({point['first_entry']:.3f},{point['continuation_stability']:.3f})}};"
        )
        lines.append(
            rf"\node[font=\scriptsize,fill=white,fill opacity=0.96,text opacity=1,inner sep=1.15pt,rounded corners=1pt,"
            rf"draw={color}!45,line width=.22pt] at (axis cs:{point['first_entry'] + dx:.3f},{point['continuation_stability'] + dy:.3f}) {{{label}}};"
        )
    lines.extend(
        [
            r"\end{axis}",
            r"\end{tikzpicture}",
            "",
        ]
    )
    return "\n".join(lines)


def main() -> int:
    # The compact release archive does not include the broad multi-seed sweep store
    # required to regenerate this figure from scratch. The generated TikZ is already
    # present at paper/figures/fig_stage_axis_map_tikz.tex.
    sweep_root = ROOT / "research/results/four_lang_thesis_panel"
    if not sweep_root.exists():
        print("NOTE: Full sweep store not found in this compact archive.")
        print(f"      The generated figure is already at: {OUT_TEX.relative_to(ROOT)}")
        print("      Full regeneration requires the broad multi-seed result store.")
        return 0

    points = [_point(model, lang) for model in ["1b", "4b"] for lang in LANGS]
    FIG_DIR.mkdir(parents=True, exist_ok=True)
    tikz = _tikz(points)
    OUT_TEX.write_text(tikz, encoding="utf-8")
    OUT_JSON.parent.mkdir(parents=True, exist_ok=True)
    OUT_JSON.write_text(json.dumps({"points": points}, indent=2, ensure_ascii=False), encoding="utf-8")
    print(f"Wrote {OUT_TEX}")
    print(f"Wrote {OUT_JSON}")
    for p in points:
        print(
            f"{p['model_label']:>2} {p['language']:<16} x={p['first_entry']:.3f} "
            f"tailCER={p['tail_cer']:.3f} y={p['continuation_stability']:.3f} seeds={p['n_seeds']}"
        )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
